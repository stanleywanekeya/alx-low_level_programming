#include "3-calc.h"

/**
 * op_add - add parameters
 * @a: integer parameter
 * @b: integer parameter
 * Return: sum
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtracts parameters
 * @a: integer parameter
 * @b: integer parameter
 * Return: value of parameter after subtraction
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplies parameters
 * @a: integer parameter
 * @b: integer parameter
 * Return: parameter multiplication
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - divides parameter
 * @a: integer parameter
 * @b: integer parameter
 * Return: value of division
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - modulos of parameter
 * @a: integer parameter
 * @b: integer parameter
 * Return: value of modulos
 */
int op_mod(int a, int b)
{
	return (a % b);
}
